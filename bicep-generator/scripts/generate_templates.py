def main():
    templates_dir = root / "templates"
    os.makedirs(templates_dir, exist_ok=True)

    for provider, api_version in PROVIDERS.items():
        print(f"Fetching schema for {provider} ({api_version})...")
        try:
            schema = SchemaFetcher.fetch_schema(provider, api_version)
            definitions = schema.get("resourceDefinitions", {})
            
            provider_dir = templates_dir / provider
            os.makedirs(provider_dir, exist_ok=True)

            for res_type in definitions.keys():
                bicep_res_type = res_type.replace("_", "/")
                full_type = f"{provider}/{bicep_res_type}"
                template_path = provider_dir / f"{res_type}.j2"
                
                print(f"  Generating template for {full_type}...")
                template_content = SchemaFetcher.generate_template_from_schema(schema, full_type)
                
                with open(template_path, "w", encoding="utf-8") as f:
                    f.write(template_content)
                    
        except Exception as e:
            print(f"Error processing {provider}: {e}")

if __name__ == "__main__":
    main()
